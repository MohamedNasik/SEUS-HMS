        $(document).ready(function () {

            // change comment to database
            $(document).on('click', '#change', function () {

                var med_name = $('#med_name').val();
                var med_description = $('#med_description').val();
                var type = $('#type').val();
                var des_id = <?php echo $_GET['did'];  ?>

                var valid;
                valid = validateContact();

                if (valid) {

                    swal({
                        title: "Are you sure?",
                        text: "You wanna change this Medicine!",
                        type: "warning",
                        showCancelButton: true,
                        confirmButtonClass: "btn-success",
                        confirmButtonText: "Yes, Change It!",
                        closeOnConfirm: false
                    },
                        function (isConfirm) {
                            if (!isConfirm) return;
                            $.ajax({
                                url: "adminquery/update_medicine.php", // Url to which the request is send
                                type: "POST",             // Type of request to be send, called as method
                                data: {
                                    'med_name': med_name,
                                    'med_description': med_description,
                                    'des_id': des_id,
                                    'type': type,

                                },
                                success: function (data) {
                                    if (data == 'Medicine Updated Successfully') {
                                        swal("Success", "Updated Successfully :)", "success");


                                    } else {
                                        //our handled error
                                        swal("Sorry", "Failed to Updated Decease. Try it later :(", "error");
                                    }
                                },
                                error: function (data) {
                                    //other errors that we didn't handle
                                    swal("Sorry", "Failed to send order. Please try later :(", "error");
                                }


                            });

                        });



                };

                // check validations
                function validateContact() {
                    var valid = true;
                    $(".demoInputBox").css('background-color', '');
                    $(".info").html('');

                    if (!$("#med_name").val()) {
                        $("#dess_info").html("(Required)");
                        //   $("#testing_charge").css('background-color', '#FFFFDF');
                        valid = false;
                    }
                    if (!$("#med_description").val()) {
                        $("#des_info").html("(Required)");
                        // $("#testing_description").css('background-color', '#FFFFDF');
                        valid = false;
                    }

                    if ($("#type").val() == 'Select') {
                        $("#typeposs").html("(Please select appropriate type)");
                        valid = false;
                    }



                    return valid;



                }

            });

        });





